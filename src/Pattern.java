public class Pattern {
    public static void main(String[] args) {

        for (int i = 8;i>0;i--){
            for (int j =1;j<=i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
        for (int k=1;k<8;k++){
            for (int f=1;f<=k;f++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
